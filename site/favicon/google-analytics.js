window.onload = function init() {
  var keyhole = document.querySelector(".keyhole");
  keyhole.addEventListener('click', validate);
}

function validate() {
  var key = new Array(),
      value = new Array(),
      login,
      password,
      ok = false;

  key[0]   = "полуфабрикаты";
  value[0] = "1423532";
  key[1]   = "ЯнелегальноХакнул";
  value[1] = "ИменяЗабанятПоАйпи";

  login = prompt("Emerald Queen: Stop right there, criminal scum! Nobody breaks the law on my watch!", "Кодовое слово") || "";
  password = prompt("Emerald Queen: It's all over, lawbreaker! Your spree is at an end.", "Пароль") || "";

  setTimeout(function server() {
    if (login == key[0] && password == value[0]) {
      ok = true;
      alert("Emerald Queen: Lion's Rest...");
      window.location = "../rip";
    }

    if (login == key[1] && password == value[1]) {
      ok = true;
      alert("Emerald Queen: Marina, ave Marina!");
      window.location = "Avemarine2016.exe";
    }

    if (ok == false) {
      alert("Emerald Queen: You are not prepared!");
      window.location = "../";
    }
  }, 1_000);
}

